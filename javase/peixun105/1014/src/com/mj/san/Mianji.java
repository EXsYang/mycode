package com.mj.san;

public class Mianji {

	private int a;
	private int b;	
	private int sum;
	public int getSum() {
		return sum;
	}

	public void setSum(int sum) {
		this.sum = sum;
	}

	
 
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	@Override
	public String toString() {
		return "Mianji [a=" + a + ", b=" + b + ", sum=" + sum + "]";
	}

	
	
	
}
